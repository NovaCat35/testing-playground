function add(num1,num2) {
   const sum = num1 + num2
   return sum
}

function subtract(num1,num2) {
   const total = num1 - num2
   return total;
}

function divide(num1, num2) {
   const total = num1/num2
   return total;
}

function multiply(num1, num2) {
   const total = num1*num2
   return total;
}
export {add, subtract, divide, multiply}